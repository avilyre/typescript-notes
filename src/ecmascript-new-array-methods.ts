/**
 * Method: Array.toSorted();
 * The original array it's not modified and return a new array with it's value modified
 */
const numbers = [5, 1, 4, 3, 2];
const result = numbers.toSorted();
console.log("Array.toSorted():", result);

/**
 * Method: Array.toReversed();
 * The original array it's not modified and return a new array with it's value modified
 */
const numbers2 = [5, 1, 4, 3, 2];
const result2 = numbers.toReversed();
console.log("Array.toReversed():", result2);
